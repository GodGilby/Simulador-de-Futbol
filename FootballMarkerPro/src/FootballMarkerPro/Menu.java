    private void cbLigaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbLigaItemStateChanged
        if(evt.getStateChange() == ItemEvent.SELECTED)
        {
            if(this.cbLiga.getSelectedIndex()> 0)
            {
                this.cbEquipo.setModel(new DefaultComboBoxModel(this.getEquipo(this.cbLiga.getSelectedItem().toString())));
            }
        }
        
         
    }//GEN-LAST:event_cbLigaItemStateChanged

