public String[] getEquipo(String liga)
    {
     String[] equipo = new String[3];
     if(liga.equalsIgnoreCase("España"))
     {
         equipo[0] = "Real madrid";
         equipo[1] = "Barcelona";
         equipo[2] = "Atletico de Madrid";
                 
         
     }
     if(liga.equalsIgnoreCase("Alemania"))
     {
         equipo[0] = "Bayern Munich";
         equipo[1] = "Borussia Dortmund";
         equipo[2] = "Shalke 04";
         
     }
     if(liga.equalsIgnoreCase("Francia"))
     {
         equipo[0] = "Psg";
         equipo[1] = "Monaco";
         equipo[2] = "Lyon";
         
     }
     if(liga.equalsIgnoreCase("Mexico"))
     {
         equipo[0] = "Chivas";
         equipo[1] = "America";
         equipo[2] = "Cruz Azul";
         
     }
     if(liga.equalsIgnoreCase("Inglaterra"))
     {
         equipo[0] = "Chelsea";
         equipo[1] = "Manchester United";
         equipo[2] = "Liverpool";
         
     }
     if(liga.equalsIgnoreCase("Italia"))
     {
         equipo[0] = "Milan";
         equipo[1] = "Juventus";
         equipo[2] = "Inter";
         
     }
     return equipo;
    }
